// مساعدات للوصول إلى البيانات
export const getRiskById = (id: string): Risk | undefined => {
  return risks.find(risk => risk.id === id);
};

export const getRisksBySeverity = (severity: string): Risk[] => {
  return risks.filter(risk => risk.severity === severity);
};

export const getHighRisks = (): Risk[] => {
  return getRisksBySeverity('عالية');
};

